package com.jsservey.model;

public class Answer {
			String id;
			String answer_name;
			public String getId() {
				return id;
			}
			public void setId(String id) {
				this.id = id;
			}
			public String getAnswer_name() {
				return answer_name;
			}
			public void setAnswer_name(String answer_name) {
				this.answer_name = answer_name;
			}
}
